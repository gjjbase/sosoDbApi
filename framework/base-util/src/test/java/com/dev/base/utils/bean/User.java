package com.dev.base.utils.bean;

public class User{
	private Addr addr;
	private String name;
	private int age;
	
	public Addr getAddr() {
		return addr;
	}
	
	public void setAddr(Addr addr) {
		this.addr = addr;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
}